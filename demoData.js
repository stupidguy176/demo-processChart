      res.GrossProfit += parseFloat(val.GrossProfit)
      res.ProfitTotal += parseFloat(val.ProfitTotal)
      res.ProfitTrades += parseInt(val.ProfitTrades)
      res.TradesTotal += parseInt(val.TradesTotal)
      return res
